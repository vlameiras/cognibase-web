// Tooltip context
type TooltipContextType = {
  showTooltip: (text: string, x: number, y: number) => void;
  hideTooltip: () => void;
  tooltipText: string;
  tooltipPosition: { x: number, y: number };
  isVisible: boolean;
};

const TooltipContext = createContext<TooltipContextType | undefined>(undefined);

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function TooltipProvider({ children }: { children: ReactNode }) {
  const [tooltipText, setTooltipText] = useState('');
  const [tooltipPosition, setTooltipPosition] = useState({ x: 0, y: 0 });
  const [isVisible, setIsVisible] = useState(false);

  const showTooltip = (text: string, x: number, y: number) => {
    setTooltipText(text);
    setTooltipPosition({ x, y });
    setIsVisible(true);
  };

  const hideTooltip = () => {
    setIsVisible(false);
  };

  return (
    <TooltipContext.Provider value={{ showTooltip, hideTooltip, tooltipText, tooltipPosition, isVisible }}>
      {children}
      {isVisible && (
        <div 
          className="fixed bg-gradient-to-br from-blue-900 to-slate-900 text-white p-4 rounded-md shadow-xl z-50 max-w-xs text-sm border border-blue-500/30"
          style={{ 
            left: `${tooltipPosition.x}px`, 
            top: `${tooltipPosition.y}px`,
            transform: 'translate(-50%, -100%)',
            marginTop: '-10px',
            backdropFilter: 'blur(8px)',
          }}
        >
          {tooltipText}
          <div className="absolute w-3 h-3 bg-blue-900 rotate-45 border-r border-b border-blue-500/30" style={{ bottom: '-6px', left: '50%', transform: 'translateX(-50%)' }}></div>
        </div>
      )}
    </TooltipContext.Provider>
  );
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  // Always start with a default language for server-side rendering
  const [language, setLanguageState] = useState<Language>('en');

  // Load language preference from localStorage and URL hash only on the client side after initial render
  React.useEffect(() => {
    const handleHashChange = () => {
      const hash = window.location.hash;
      if (hash === '#pt') {
        setLanguageState('pt');
        localStorage.setItem('zettabase-language', 'pt');
      } else if (hash === '#en') {
        setLanguageState('en');
        localStorage.setItem('zettabase-language', 'en');
      } else {
        const savedLanguage = localStorage.getItem('zettabase-language');
        if (savedLanguage === 'en' || savedLanguage === 'pt') {
          setLanguageState(savedLanguage);
        } else {
          setLanguageState('en'); // Default to English if no hash or localStorage preference
        }
      }
    };

    // Initial check
    handleHashChange();

    // Listen for hash changes
    window.addEventListener('hashchange', handleHashChange);

    // Cleanup listener on component unmount
    return () => {
      window.removeEventListener('hashchange', handleHashChange);
    };
  }, []);

  // Custom setLanguage function that also updates localStorage
  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('zettabase-language', lang);
  };

  // Translation function
  const t = (key: string): string => {
    return translations[language][key as keyof typeof translations[typeof language]] || key;
  };

  return (
    <TooltipProvider>
      <LanguageContext.Provider value={{ language, setLanguage, t }}>
        {children}
      </LanguageContext.Provider>
    </TooltipProvider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

export function useTooltip() {
  const context = useContext(TooltipContext);
  if (context === undefined) {
    throw new Error('useTooltip must be used within a TooltipProvider');
  }
  return context;
}
